package com.slepeweb.cms.bean.guidance;

public class ExampleInput {
	private String example, explanation;
	
	public ExampleInput(String a, String b) {
		this.example = a;
		this.explanation = b;
	}

	public String getExample() {
		return example;
	}

	public String getExplanation() {
		return explanation;
	}
}
